# Operador Logico AND o "Y"
# Ambas condiciones para obtener un resultado deben ser verdaderas

print(True & True)
print(True & False)
print(False & True)
print(False & False)

# Operador logico OR u O
# Es verdadero cuando al menos un valor es verdadero

print(True | True)
print(True | False)
print(False | True)
print(False | False)

